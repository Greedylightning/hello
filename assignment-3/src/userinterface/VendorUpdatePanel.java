    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        // TODO add your handling code here:
        product.setModelNumber(txtModelNumber.getText());
        product.setVendorName(txtVendorName.getText());
        product.setBasePrice(txtBasicPrice.getText());
        product.setFloorPrice(txtFloorPrice.getText());
        product.setCeilingPrice(txtCeilingPrice.getText());
        product.setDescription(txtDescription.getText());
        product.setFeatures(txtFeatures.getText());
        product.setCategory((String) cmbCategory.getSelectedItem());

        JOptionPane.showMessageDialog(null, "Product successfully Updated!");
        
        txtModelNumber.setText("");
       // txtVendorName.setText("");
        txtDescription.setText("");
        txtBasicPrice.setText("");
        txtCeilingPrice.setText("");
        txtFloorPrice.setText("");
        txtFeatures.setText("");
    }//GEN-LAST:event_btnConfirmActionPerformed

